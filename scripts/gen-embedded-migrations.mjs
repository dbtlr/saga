#!/usr/bin/env node
// Regenerate packages/db/src/embedded-migrations.ts — the drizzle migrations
// inlined as plain TS string literals so a compiled single-binary (no repo
// tree) can still read them. Run after adding or editing a migration; the
// packages/db drift-lock unit test fails until the embedded set matches
// packages/db/drizzle byte-for-byte.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(fileURLToPath(new URL('..', import.meta.url)));
const drizzleDir = join(repoRoot, 'packages/db/drizzle');
const journal = readFileSync(join(drizzleDir, 'meta/_journal.json'), 'utf8').trim();
const sqlFiles = readdirSync(drizzleDir)
  .filter((file) => file.endsWith('.sql'))
  .toSorted();

const lines = [
  '// GENERATED by scripts/gen-embedded-migrations.mjs — do not edit by hand.',
  '// Drizzle migrations inlined as literals so a compiled single-binary (no repo',
  '// tree) can still read them. Regenerate after adding a migration.',
  '',
  `export const embeddedJournal = ${journal};`,
  '',
  'export const embeddedSql: Record<string, string> = {',
];
for (const file of sqlFiles) {
  const tag = file.replace(/\.sql$/, '');
  lines.push(
    `  ${JSON.stringify(tag)}: ${JSON.stringify(readFileSync(join(drizzleDir, file), 'utf8'))},`,
  );
}
lines.push('};');

writeFileSync(join(repoRoot, 'packages/db/src/embedded-migrations.ts'), `${lines.join('\n')}\n`);
process.stdout.write(
  `wrote packages/db/src/embedded-migrations.ts (${sqlFiles.length} migrations)\n`,
);
